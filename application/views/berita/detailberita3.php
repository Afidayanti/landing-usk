        <div class="mt-2" style="text-align: justify; font-color: black;">

        <p>MAKASSAR Atsiri Research Center (ARC) yang notabene Pusat Unggulan Iptek Perguruan Tinggi (PUIPT) Nilam Aceh Universitas Syiah Kuala (USK) memberikan pelatihan produk inovasi nilam kepada UMKM di Makasar. Pelatihan yang diikuti oleh 20 orang peserta tersebut dilaksanakan di Hotel Kryad Haka Makasar, bekerja sama dengan Yayasan Hadji Kalla. Pelatihan ini berlangsung sejak 18 Juli 2022 hingga 20 Juli 2022.</p>

        <p>Tim instruktur dipimpin oleh Kepala ARC-USK, Syaifullah Muhammad dengan anggota Nadia Isnaini, Adinda Gusti Vonna dan Nanda Funna Ledita.

</p> <p>Dalam Pelatihan selama tiga hari penuh tersebut telah diberikan berbagai materi mengenai dasar-dasar pengembangan nilam dari hulu ke hilir, praktek langsung pengembangan produk berbasis minyak nilam yang telah dipurifikasi seperti parfum, sabun, medicated oil dan body butter. Juga diberikan materi terkait Business Model Canvas (BMC), branding, digital marketing, pengelolaan keuangan UMKM, Pengurusan Izin BPOM dan HAKI.</p>

        <p>Selama pelatihan peserta memproduksi langsung produk inovasi nilam dibawah bimbingan instruktur ARC yang berpengalaman. Peserta juga diberikan alat produksi agar bisa terus mengembangkan usaha dimasa yang akan datang.</p>

        <p>Manajer Bidang Ekonomi dan Sosial Hadji Kalla, Herni dalam sambutannya menyampaikan apresiasi kepada ARC dan peserta pelatihan yang selama 3 hari penuh mengikuti berbagai materi yang diberikan. Ia menjelaskan, kegiatan ini telah lama direncanakan oleh Yayasan Hadji Kalla dan saat ini telah berhasil dilaksanakan.

</p><p>"Direktur Eksekutif Yayasan Hadji Kalla sendiri yang meminta kami untuk melaksanakan pelatihan ini bekerjasama dengan ARC. Beliau pernah mengikuti seminar yang nara sumbernya Kepala ARC, Syaifullah Muhammad, kemudian dilanjutkan membuat MoU dan MoA dengan Rektor USK dan ARC," tutur Herni.



</p><p>Kegiatan tersebut dibiayai oleh CSR Kalla Group yang dilaksanakan oleh Yayasan Hadji Kalla.Tujuan dari pelatihan ini diharapkan bisa memberikan area baru bagi masyarakat Sulawesi khususnya UMKM, untuk mendapatkan penghasilan.

</p><p>"Kami berharap, pengetahuan yang diperoleh selama 3 hari ini baik teori maupun praktek langsung, dapat dikembangkan lebih lanjut kepada masyarakat desa di berbagai pelosok Sulawesi" tutupnya.



</p><p>Sementara itu Kepala ARC, Syaifullah Muhammad dalam paparannya menyampaikan bahwa Sulawesi merupakan salah satu produsen nilam terbesar di Indonesia terutama Sulawesi Tengah, Sulawesi Tenggara dan Sulawesi Barat, juga ada sebagian kecil di Sulawesi Selatan.



</p><p>Beberapa perusahaan besar telah lama membina petani, penyuling dan membeli langsung minyak nilam dari masyarakat dan pengumpul. Hanya saja seluruh produk yang diperdagangkan adalah minyak nilam mentah (Crude Patchouli)  yang diperoleh dari penyulingan secara tradisional melalui ekstraksi uap panas (steam).

</p><p>"Di Sulawesi belum ada sentuhan inovasi untuk purifikasi (pemurnian) dan peningkatan kualitas minyak nilam yang dikembangkan menjadi produk turunan nilam seperti parfum, medicated oil aroma terapi, body butter dan lain-lain," urai Syaifullah

</p><p>Menurutnya, pengembangan produk turunan selait meningkatkan nilai tambah, pembukaan lapangan kerja, peningkatan penghasilan masyarakat, juga akan membentuk ekosistem baru dari tataniaga nilam yang lebih berkeadilan untuk semua stake holders, dimana harga minyak nilam akan relatif stabil seiring meningkatnya kebutuhan minyak nilam akibat munculnya start up baru atau UMKM yang menggunakan nilam sebagai bahan baku produk inovasi mereka.</p>

<p>"Ini akan berdampak positif untuk jangka panjang khususnya kepastian supply bahan baku nilam secara berkelanjutan," ucapnya.

</p>

<p>"Ini akan berdampak positif untuk jangka panjang khususnya kepastian supply bahan baku nilam secara berkelanjutan," ucapnya.

</p>

<p>Doktor Teknik Kimia lulusan Australia itu menerangkan, bersama Yayasan Hadji Kalla, pihaknya akan mengupayakan mereplika keberhasilan ARC membangun ekosistem baru (Blue Ocean) nilam dengan pendekatan Pentahelix di Aceh untuk diterapkan di Sulawesi. Sehingga UMKM produk turunan nilam akan berkembang juga di Sulawesi.

</p>

<p>"Sudah saatnya minyak nilamnya dari negeri kita dan parfumnya pun ada di negeri kita juga. Nilam from seed to seal harus terjadi di negeri kita sendiri," pungkas Syaifullah.

</p>

<p>Kegiatan pelatihan ditutup secara resmi Rabu Sore, 20 Juli 2022 oleh Herni mewakili Direktur Eksekutif Yayasan Hadji Kalla. Penutupan ditandai dengan penyerahan sertifikat, alat produksi dan juga ratusan botol empat produk yang diproduksi selama pelatihan untuk bisa segera dipasarkan.

</p>



