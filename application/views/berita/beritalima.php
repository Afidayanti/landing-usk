            <p>Banda Aceh (ANTARA) - Perusahaan perawatan kulit (skin care) lokal Aceh, PT Biona Ceudah Rupa binaan Badan Riset dan Inovasi Nasional (BRIN) dalam program PPBR (Perusahaan Pemula Berbasis Riset) masuk semifinal kompetisi China-ASEAN Innovation and Entrepreneurship Competition (CAIEC) bidang Bio-Medicine.</p>   
            <p>CEO Biona Ceudah Rupa, Adinda Gusti Vonna, di Banda Aceh, Senin, mengatakan perusahaannya tersebut selama ini melakukan komersialisasi hasil riset dari ARC PUI-PT Nilam Universitas Syiah Kuala (USK) Banda Aceh.</p>   
            <p>CEO Biona Ceudah Rupa, Adinda Gusti Vonna, di Banda Aceh, Senin, mengatakan perusahaannya tersebut selama ini melakukan komersialisasi hasil riset dari ARC PUI-PT Nilam Universitas Syiah Kuala (USK) Banda Aceh.</p>

            <p>"Kami berterima kasih pada ARC-USK atas kesempatan mengembangkan produk turunan nilam hasil riset ini. Semoga menjadi salah satu kebanggaan inovasi dari Aceh," kata Adinda Gusti Vonna.</p>

            <p>Dinda menyampaikan, perhelatan semifinal itu akan berlangsung pada 21-22 November 2022 di Singapura. Kompetisi ini merupakan ajang <i>pitching</i> yang bertujuan untuk mengasah kemampuan perusahaan dalam mencari investor.</p>

            <p>"Bila presentasi di Singapura berhasil hingga babak final, maka setiap peserta akan melanjutkan kompetisi di Nanning, China," ujarnya.</p>

            <p>Dinda mengucapkan terima kasih atas kesempatan presentasi Biona di Singapura itu. Semua persiapan sudah dilakukan dengan sebaik mungkin, diharapkan ke depan benar-benar bisa go internasional.</p>

            <p>Sementara itu, Koordinator Fungsi Inkubasi PPBR BRIN Ferry Ramadhan menyampaikan apresiasinya terhadap Biona, meskipun belum setahun diinkubasi oleh BRIN sudah mampu berkompetisi di tingkat internasional.</p>

            <p>"Biona terpilih dari sekian pemenang PPBR 2022 dari seluruh Indonesia, karena memiliki keunggulan komparatif dan kompetitif dalam produknya," katanya.</p>

            <p>Fery berharap Biona bisa mengharumkan nama Aceh dan Indonesia serta menang hingga ke babak final. Saat ini Biona memiliki 4 produk <i>skin care </i>masing-masing<i> Facial wash, toner, serum antiaging <i>dan</i> moisturizer.</i></p>

            <p>"Semua produk ini menggunakan fraksi ringan minyak nilam Aceh yang telah diproses secara molecular distillation di ARC-USK," ujar Fery.</p>

            <p>Untuk diketahui, selama lebih dari enam tahun, ARC USK telah berhasil merintis jalan baru industri nilam Aceh dengan berbagai inovasi hulu hilir nilam.</p>

            <p>Kemudian, juga meningkatkan pelatihan dan pembinaan ribuan anak muda serta UMKM Aceh untuk membentuk start up bisnis nilam, sebagai upaya terciptanya ekosistem baru (blue ocean) bagi tata niaga nilam Aceh.*</p>
