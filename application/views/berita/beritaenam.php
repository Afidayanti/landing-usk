    <p>Pandemi Covid-19 beberapa tahun silam, menjadi momentum bagi Ambri Agusmanto dan sejawatnya untuk memproduksi hand sanitizer, atau pembersih tangan. Kala itu, kebutuhan masyarakat terhadap antiseptik meningkat pesat, seriring dengan kasus yang meningkat.
</p>   
    <p>Mendirikan Industri Kecil Menengah (IKM), Ambri dan rekannya kemudian memulai produksi hand sanitizer, dan memberi nama produknya Cantila. Jadi, nama Cantila itu merupakan singkata dari Cairan Anti Kuman Nilam Aceh, paparnya.
</p>   
    <p>Kebanyakan hand sanitizer yang beredar di pasaran, berbahan baku alkohol, nah ini yang kemudian membuat Cantila berbeda. Menggunakan ekstrak nilam dan minyak sereh jadi keunggulan produk kami, tukas Ambri lagi.</p>   
    <p>IKM Cantila sendiri, dirintis oleh empat orang mahasiswa USK, yakni Ambri Agusmanto, Weni Fitri Erfar, Chindy Mauliza Duana dan Hurum Maqshurah. Mulai menjalankan produksi pada Juni 2019 silam.</p>   
    <p>Sebab menggunakan bahan baku nilam dan sereh wangi, Ambri mengklaim Cantila lebih efektif membunuh kuman, dan bakteri. Karenanya sangat cocok digunakan di fasilitas kesehatan, rumah tangga, dan juga di sekolah, serta rumah sakit.</p>   
    <p>Keunggulan lainnya, Sambung dia, produk Cantila ramah lingkungan, dan tidak lengket saat digunakan, serta tidak ada efek panas, sebab tidak menggunakan alkohol.
</p>   
    <p>“Keunggulan ini yang membuat Cantila lebih disukai konsumen,” terang Ambri saat bincang dengan popularitas.com beberapa waktu lalu.

</p>   
    <p>Cantila sendiri, selain menggunakan produk nilam dan sereh wangi, dalam proses produksinya juga menggunakan aquades, carbopol 940, propilen glikol, gliserin, trietanolamin (TEA), dan bibit parfum untuk memberi efek wangi.
</p>   
    <p>Saat ini, Cantila yang beredar di pasaran tersedia dalam ukuran 30 ML, dan dijual seharga Rp20 ribu. Ambri sendiri, selain manfaatkan media sosial untuk pasarkan produknya, menjual secara door to door dirinya kepada dosen, dan pihak lainnya yang membutuhkan hand sanitizer. 
</p>   
    <p>Untuk mendapatkan bahan baku dalam memproduk Cantila, pihaknya bekerjasama dengan Atsiri Research Centre (ARC) USK, dan kini IKM Cantila telah memiliki lima pekerja untuk membantu dalam proses produksi hand sanitizer.
</p>   
