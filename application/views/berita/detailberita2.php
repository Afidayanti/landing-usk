        <div class="mt-2" style="text-align: justify; font-color: black;">

    <p>Banda Aceh (ANTARA) - Komunitas Aneuk Muda Unggul dan Hebat (Amanah) bersama Atsiri Research Center (ARC) Universitas Syiah Kuala (USK) Banda Aceh dan PUIPT Nilam Aceh memberdayakan generasi milenial untuk mengembangkan ekonomi kreatif berbasis minyak nilam Aceh. </p>

    <p>"Pengembangan itu dimulai dengan pelatihan empat produk turunan nilam yaitu parfum, body butter, pomade dan sabun body wash kepada 72 peserta," kata Ketua Amanah Muhammad Tanwier, di Banda Aceh, Kamis.</p>

    <p>Tanwier menyampaikan, nilam salah satu dari sembilan komoditas unggulan Aceh yang bernilai ekonomi tinggi, karena menjadi komoditas ekspor dan bahan baku berbagai produk industri di mancanegara. </p>

    <p>Nilam Aceh diharapkan dapat menjadi penggerak ekonomi lokal melalui generasi milenial.</p>

    <p>“Pelatihan produk turunan Nilam untuk milenial ini adalah upaya untuk memajukan industri Nilam Aceh," ujar Kepala Dinas Perindustrian dan Perdagangan Aceh itu. </p>

    <p>Tanwier menuturkan, nilam juga komoditas unggulan Aceh yang sudah banyak dilupakan masyarakat, sampai kemudian melalui berbagai upaya yang dilakukan ARC nilam Aceh mulai bangkit kembali. </p>

    <p>Sementara itu, Rektor Universitas Syiah Kuala Prof Marwan berharap peserta milenial dalam pelatihan ini dapat berkembang menjadi start up bisnis berbasis minyak nilam yang maju sehingga memberi nilai tambah dan pergerakan ekonomi lokal Aceh. </p>

    <p>"USK melalui ARC akan senantiasa mendampingi dan memberikan dukungan iptek dan inovasi serta jaringan baik lokal, nasional maupun internasional," kata Prof Marwan. </p>



    <p>Dirinya menuturkan, nilam Aceh memiliki sejarah panjang sejak awal 1900-an. Dengan berbagai upaya yang dilakukan dalam lima tahun terakhir, ekosistem baru nilam Aceh mulai terbentuk dan memberi kebaikan untuk semua stakeholder nilam. </p>

    <p>USK saat ini terus mengembangkan inovasi dan menjalin kemitraan dengan berbagai pihak agar kejayaan nilam Aceh dapat diraih kembali. </p>

    <p>Keunggulan USK dalam SDM, riset dan inovasi akan terus didedikasikan untuk kemajuan masyarakat.</p>

    <p>"Inovasi hulu-hilir nilam yang memberi dampak positif terhadap ekonomi masyarakat, dapat direplika untuk membangun ekosistem komoditas unggulan lainnya seperti padi, jagung, pala, sereh wangi dan lain-lain” tutup Marwan. </p>

    <p>Pelatihan produk turunan nilam untuk empat produk inovasi berbasis minyak nilam tersebut berlangsung di rumah produksi ARC yang telah tersertifikasi BPOM dan dapat menerima maklon produk dari pihak ketiga. </p>

<p>Diharapkan iklim usaha UMKM Aceh semakin kondusif dan memberikan dampak ekonomi terhadap masyarakat dan daerah.</p>

    

