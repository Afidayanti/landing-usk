        <h3>Pemesanan Gedung (Booking)</h3>
            <ul class="small">
                <li>Setiap penyewa yang menggunakan gedung AAC untuk berbagai kegiatan, harus melakukan pendaftaran (reservasi) melalui staf administrasi pengelola gedung. Dan membayar uang panjar sebesar 30% dari total biaya sewa.</li>
