                <li>Penyewa meluasi semua biaya sewa paling telat 3 (tiga) hari sebelum gedung digunakan.</li>
                <li>Bila terjadi pembatalan sewa oleh pihak penyewa, maka uang muka yang telah dibayarkan tidak dapat dimintakan kembali dan menjadi hak pengelola gedung seluruhnya. </li>

